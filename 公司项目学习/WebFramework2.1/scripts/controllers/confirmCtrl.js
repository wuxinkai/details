define(['ngload!ui.bootstrap','angular-confirm'], function () {
    'use strict';
    return ['$scope', '$confirm', function ($scope, $confirm) {
        $scope.showConfirm = function () {
            $confirm({ text: '这是个自定义确认框，是否确认。' })
             .then(function () {
                 $scope.result = "确认";
             }, function () {
                 $scope.result = "取消";
             })
        }
    }]
});